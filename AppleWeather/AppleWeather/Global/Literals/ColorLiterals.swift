import UIKit

extension UIColor {
    static var WeatherWhite: UIColor {
        return UIColor(hex: "#FFFFFF")
    }
    
    static var WeatherGray1: UIColor {
        return UIColor(hex: "#8C8C8C")
    }
    
    static var WeatherGray2: UIColor {
        return UIColor(hex: "#1A1A1A")
    }
}
